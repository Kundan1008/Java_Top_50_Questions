import java.util.Scanner;

//Swapping two numbers without using third variable
public class Program2 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int b = sc.nextInt();
		System.out.println("Before swapping num1 is : " + a + " and " + "num2 is: " + b);
		b = b + a;
		a = b - a;
		b = b - a;
		System.out.println("After swapping num1 is : " + a + " and " + "num2 is: " + b);
		sc.close();
	}

}
